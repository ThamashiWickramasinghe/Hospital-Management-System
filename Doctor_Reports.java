   private void loadReportData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        File file = new File("report.txt");
        if (!file.exists()) {
            JOptionPane.showMessageDialog(this, "Error: report.txt not found!", "File Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] data = line.split(","); // Assuming values are comma-separated
                if (data.length == 3) {
                    model.addRow(new Object[]{
                        data[0].trim(),  
                        Integer.parseInt(data[1].trim()),  
                        data[2].trim(),  
                        
                    });
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading schedule file!", "Read Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid data format in report.txt!", "Data Error", JOptionPane.ERROR_MESSAGE);
        }
    }


